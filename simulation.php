<u><b>Caution (Joo-Eui)</b></u>
<ol>
	<li>Call for Kal-yeo. (Break)</li>
	<li>Stretch the right arm with the interior angle being 135 degrees and point at the forehead of the pertinent contestant with the forefinger and call for Chung (Blue) or Hong (Red)</li>
	<li>Turn the right palm forward and sway it from the center to left, left to right, right to center (Belt level with the arm fully extended).</li>
	<li>Get the right fist clenched with only forefinger stretched (to let the pertinent contestant that the Referee has given a caution)</li>
	<li>Call for Kye-sok. (Continue)</li>
</ol>
<u><b>Annulment Of Points Scored</b></u>
<ol>
	<li>Call for Kal-yeo. (Break)</li>
	<li>In "Cha-ryeot" stance, raise the right palm before the forehead with 20 cm distance from the forehead</li>
	<li>Turn the right palm forward and sway it from the center to right, right to left, left to center; to annul the point(s) scored</li>
	<li>Show the sign of "Shi-gan" (Stop time) to the Recorder's Desk and declare the penalty to the pertinent contestant and then call for "Kye-sok". (Continue)</li>
</ol>
<u><b>Kye-Shi (Medical Time Out)</b></u>
<ol>
	<li>After the declaration of "Kal-yeo" (Break), the referee shall position himself/herself facing the recorder before declaring "Kye-Shi".</li>
	<li>40 seconds after the declaration of "Kye-Shi" the referee shall begin to loudly announce the passage of time in 5 seconds intervals up to 60 seconds (40, 45, 50, 55, 60).</li>
</ol>
<u><b>Keu-Man (Stop)</b></u>
<ol>
	<li>After the declaration of "Keu-man", the referee shall position himself/herself facing the recorder before sending them back to their coaches' mark.</li>
	<li>When "Keu-Man" is called and the contestants are not on their side of their coaches' mark, the referee shall call for Chung and Hong to move to their respective sides by indicating to them to switch sides. The referee shall then send them to their coaches' mark.</li>
</ol>
<u><b>Knock Down</b></u>
<ul style="list-style-type: none;">
	<li>In the event of a knock down where both contestants are knocked down the referee shall continue counting as long as one or both of the contestants has not sufficiently recovered.  If both contestants do not recover then the referee shall count up to "Yeol" (Ten).<li>
</ul>

