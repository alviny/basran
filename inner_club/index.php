<p>The Annual Cheung's Taekwondo® Inner Club Tournament provides an opportunity
for members of one location to get acquainted with the uniqueness of another Cheung's
Taekwondo® location – e.g. the different technical emphasis chief instructors focus on in their
locations; the different personalities of the chief instructors and students, and the different ways
the locations express their competitive and martial art spirit. Different location approaches and
different chief instructors provide different shades and flavors. These differences are not divisive.</p>
<p>They are encouraged to exist separately and are respected for doing so. Yet they combine
together to become what is uniquely Cheung's Taekwondo®. These different shades and flavors
provide the necessary ebb and flow for ensuring a dynamic organization and not a stagnant one.</p>

<p>These Tournaments also provide an opportunity for:
<ul>
	<li>Participants are to be motivated and challenged by each other to break through self-doubts
and self-made barriers. This experience enables participants to reach higher levels of technical
training and competition accomplishment.</li>
	<li>Everyone to learn and reaffirm, by experience, that:</li>
	<ul>
		<li>Winning within oneself through participation is the true spirit of competition.</li>
		<li>Winning of a medal or a trophy is a bonus. The medal/trophy is a souvenir of a point in
time where one's technical superiority, assigned opponent, time and place of competition
all happened to combine in one's favor.</li>
	</ul>
</ul></p>

<p>To help participants realize the above Tournaments' goals, the event is conducted in a way:
<ul>
	<li>That SAFETY is the main underlying principle governing the operation of this competition
e.g. all refereeing will be carried out in a manner emphasizing fairness and safety.</li>
	<li>That good sportsmanship and emotional discipline be emphasized.</li>
	<li>That the event encourages and involves active co-operation and participation of parents,
Senior Masters, Masters, instructors, and students.</li>
</ul></p>
 
<p>AWARDS shall be in the form of medals or trophies. There will be medals or trophies in Hand
Motions, Pattern (Poomsae), Sparring and Breaking Competition for each Division for:
<ul>
	<li>1st Place</li>
	<li>2nd Place</li>
	<li>3rd Place</li>
	<li>4th Place</li>
</ul></p>

<p>Grand Champion, Pattern (Poomsae) & Sparring Trophies will also be awarded to Cheung's
Taekwondo® locations.</p>
