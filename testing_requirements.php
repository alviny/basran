<a name="faq"><h3>Frequently Asked Questions</h3></a>

<p><b>Why wasn't I recommended for testing even though I have the required number of classes?</b></p> 

<table border="0" cellpadding="5" cellspacing="0">
<tr>
	<td valign="top"><p><b>Answer:</b></p> </td>
	<td valign="top"><p>The expectation at Basran Taekwondo® is that all students train with effort, focus, concentration, and the intent to improve technically.  With this attitude in mind, students should be able to progress technically to satisfy the school's standard to test for their next rank.  Ranks represent the technical standard of an individual school as well as the particular individual.  A student who attends classes to fulfill a required number of classes, but does not demonstrate the required technical standard will not be recommended to test.  Students are also required to attend classes over a regular period of time – see below. Basran Taekwondo holds a very high standard in the Martial Arts community and the ranks are to be earned by mastering the required standard at each level.</p></td>
</tr>
</table>
           

<p><b>What is the expectation for attendance in training?</b></p> 

<table border="0" cellpadding="5" cellspacing="0">
<tr>
	<td valign="top"><p><b>Answer:</b></p></td>
	<td valign="top"><p>At the colour rank level, we require each student to attend training a minimum of 3 times per week. Depending on the belt, regular attendance will mean that the student has continuous exposure to the required skills for their rank over an 8 to 12 week period. Although a student might have the stated minimum number of classes, if those classes were attained over an extended period of time – especially if the student has been away for taken a break for a number of weeks, that student might not be recommended for testing.</p>
	
	<p><i><b>Examples:</b></i></p>
	
	<p>Three students are preparing for Green Belt testing which requires a minimum of 25 classes. </p>

	<p><b>Student A</b> has 30 classes over an 8 week (2 months) period meaning that he/she trained on average 4 times per week. Assuming technical proficiency, Student A will be recommended to test.</p>

	<p><b>Student B</b> has 30 classes over a 10 week period meaning that the student trained on average 3 times a week.  Assuming technical proficiency, Student B will be recommended to test.</p>

	<p><b>Student C</b> has 40 classes over a 20 week (5 months) period meaning that the student attended on average only 2 times a week. Student C will need to demonstrate more regular attendance in training as well as technical proficiency before being recommended to test.</p>

	</td>
</tr>
</table>

            







